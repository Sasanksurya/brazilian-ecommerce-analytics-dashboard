-- -- BUSINESS QUESTION:
-- What is the total revenue generated by the e-commerce platform?

SELECT 
    ROUND(SUM(payment_value), 2) AS total_revenue
FROM payments;
-- SUM(payment_value) adds all payment transactions
-- ROUND makes output cleaner
-- This gives total platform revenue


-- BUSINESS QUESTION:
-- How many total orders were placed?

SELECT 
    COUNT(DISTINCT order_id) AS total_orders
FROM orders;
-- DISTINCT avoids duplicate counting
-- COUNT gives total number of unique orders

-- BUSINESS QUESTION:
-- How many unique customers are present?

SELECT 
    COUNT(DISTINCT customer_unique_id) AS total_customers
FROM customers;
-- customer_unique_id represents actual unique customers
-- customer_id may repeat for multiple purchases


-- BUSINESS QUESTION:
-- What is the average order value?

SELECT 
    ROUND(AVG(payment_value), 2) AS average_order_value
FROM payments;
-- AVG calculates average transaction amount
-- Important business KPI


-- BUSINESS QUESTION:
-- Which payment methods generate the most revenue?

SELECT 
    payment_type,
    ROUND(SUM(payment_value), 2) AS revenue
FROM payments
GROUP BY payment_type
ORDER BY revenue DESC;
-- GROUP BY separates data by payment type
-- SUM calculates revenue contribution
-- ORDER BY ranks highest to lowest


-- Repeat Customers
-- BUSINESS QUESTION:
-- How many repeat customers exist?

SELECT 
    COUNT(*) AS repeat_customers
FROM (
    SELECT 
        c.customer_unique_id,
        COUNT(o.order_id) AS total_orders
    FROM customers c
    JOIN orders o 
        ON c.customer_id = o.customer_id
    GROUP BY c.customer_unique_id
    HAVING COUNT(o.order_id) > 1
) repeat_data;
-- Join customers with orders
-- Group by unique customer
-- Count number of orders
-- Filter customers with more than one order

-- Top 10 Customers by Revenue
-- BUSINESS QUESTION:
-- Identify top customers by revenue contribution

SELECT 
    c.customer_unique_id,
    ROUND(SUM(p.payment_value), 2) AS total_spent
FROM customers c
JOIN orders o 
    ON c.customer_id = o.customer_id
JOIN payments p 
    ON o.order_id = p.order_id
GROUP BY c.customer_unique_id
ORDER BY total_spent DESC
LIMIT 10;


-- Average Orders Per Customer
-- BUSINESS QUESTION:
-- What is average purchase frequency per customer?
SELECT 
    ROUND(COUNT(o.order_id) / COUNT(DISTINCT c.customer_unique_id), 2) AS avg_orders_per_customer
FROM customers c
JOIN orders o
    ON c.customer_id = o.customer_id;
    
